package printstudentdetails;

/**
 *
 * @author admin
 */
public class Student 
{
    //Encapsulation principle -[private, getters and setters]
    private String name;
    private int ID;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the ID
     */
    public int getID() {
        return ID;
    }

    /**
     * @param ID the ID to set
     */
    public void setID(int ID) {
        this.ID = ID;
    }
    
    
}
